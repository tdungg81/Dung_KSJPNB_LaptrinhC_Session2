#include <stdio.h>

int main() {
	
	int chieudai = 10;
	int chieurong = 5;
	
	int chuvihinhchunhat = 2 * (chieudai + chieurong);
	int dientichhinhchunhat = chieudai * chieurong;
	
	printf("Chu vi hinh chu nhat la: %d\n", chuvihinhchunhat);
	printf("Dien tich hinh chu nhat la: %d\n", dientichhinhchunhat);
	
	return 0; 
}
